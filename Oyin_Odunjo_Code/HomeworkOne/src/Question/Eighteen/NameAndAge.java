//Creating the abstract method for the name and age input class 
public abstract class NameAndAge {
	
	//Will return true or false if there is an uppercase 
	public abstract boolean hasUpperCase(); 
	
	//Will turn the entire array of strings to uppercase 
	public abstract void toUpperCase();
	
	//Will add ten to the age input 
	public abstract int addTen();


}
