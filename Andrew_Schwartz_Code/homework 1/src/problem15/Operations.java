package problem15;


public class Operations {

	
	public static void main(String[] args) {
		 

			int a = 125 + 24;
			int b = 125 - 24;
			int c = 125 * 24;
			int d = 125 / 24;
			int e = 125 % 24;

			System.out.println("125 + 24 = " + a);
			System.out.println("125 - 24 = " + b);
			System.out.println("125 x 24 = " + c);
			System.out.println("125 / 24 = " + d);
			System.out.println("125 mod 24 = " + e);
		}

	}